
public class SelectionDemo {
	public static void main(String[] args) {
		System.out.println();
		
		if (3 > 5) {
			System.out.println("3 greater than 5 happened!");
		}
		else if (4 > 5) {
			System.out.println("4 greater than 5 happened!");
		}
		else if (5 > 5) {
			System.out.println("5 greater than 5 happened!");
		}
        else if (6 > 5) {
            System.out.println("Of course 6 is greater than 5!");
        }
		else {
			System.out.println("No conditions met.");
		}
		System.out.println();
	}
}
