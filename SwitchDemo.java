
public class SwitchDemo {
	public static void main(String[] args) {
		System.out.println();
		
		switch (6) {
			case 3:
				System.out.println("The choice is 3...");
				break;
			case 4:
				System.out.println("The choice is 4...");
				break;
			case 5:
				System.out.println("The choice is 5...");
				break;
			case 6:
				System.out.println("The choice is 6...");
				break;
			default:
				System.out.println("No condition met...");			
		}
		
		System.out.println();
	}

}
